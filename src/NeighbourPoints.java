
public class NeighbourPoints {

	private Point image_1_point;
	private Point image_2_point;
	public NeighbourPoints(Point image_1_point, Point image_2_point) {
		this.image_1_point = image_1_point;
		this.image_2_point = image_2_point;
	}
	
	public Point getImage1Point() {
		return image_1_point;
	}
	public Point getImage2Point() {
		return image_2_point;
	}
	
	
	
	
	
}
